/*
 * Dijkstra's algorithm.
 * Verified by: AtCoder ABC164 (https://atcoder.jp/contests/abc164/submissions/12423853)
 */

struct Dijkstra {
    edges: Vec<Vec<(usize, i64)>>, // adjacent list representation
}

impl Dijkstra {
    fn new(n: usize) -> Self {
        Dijkstra { edges: vec![Vec::new(); n] }
    }
    fn add_edge(&mut self, from: usize, to: usize, cost: i64) {
        self.edges[from].push((to, cost));
    }
    /*
     * This function returns a Vec consisting of the distances from vertex source.
     */
    fn solve(&self, sources: &[usize], inf: i64) -> Vec<i64> {
        let n = self.edges.len();
        let mut d = vec![inf; n];
        // que holds (-distance, vertex), so that que.pop() returns the nearest element.
        let mut que = std::collections::BinaryHeap::new();
        for &s in sources {
            que.push((0, s));
        }
        while let Some((cost, pos)) = que.pop() {
            let cost = -cost;
            if d[pos] <= cost {
                continue;
            }
            d[pos] = cost;
            for &(w, c) in &self.edges[pos] {
                let newcost = cost + c;
                if d[w] > newcost {
                    d[w] = newcost + 1;
                    que.push((-newcost, w));
                }
            }
        }
        return d;
    }
}

trait Bisect<T> {
    fn lower_bound(&self, val: &T) -> usize;
    fn upper_bound(&self, val: &T) -> usize;
}

impl<T: Ord> Bisect<T> for [T] {
    fn lower_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] >= val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
    fn upper_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] > val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
}

fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    input! {
        n: usize, m: usize,
        abc: [(usize1, usize1, i64); m],
    }
    let mut g = vec![vec![]; n];
    for &(a, b, c) in &abc {
        g[a].push((c, b));
    }
    for i in 0..n {
        g[i].sort();
    }
    let mut offset = vec![0; n + 1];
    for i in 0..n {
        offset[i + 1] = offset[i] + g[i].len();
    }
    let mut dijk = Dijkstra::new(2 * offset[n] + 1);
    for inc in 0..2 {
        for i in 0..n {
            for j in 0..g[i].len() {
                let (cost, to) = g[i][j];
                let mut to_ex = None;
                if inc == 0 {
                    // decreasing
                    let idx = g[to].upper_bound(&(cost, 1 << 30));
                    if idx < g[to].len() {
                        to_ex = Some(offset[n] + offset[to] + idx);
                    }
                } else {
                    // increasing
                    let idx = g[to].lower_bound(&(cost, 0));
                    if idx > 0 {
                        to_ex = Some(offset[to] + idx - 1);
                    }
                }
                if let Some(to_ex) = to_ex {
                    dijk.add_edge(inc * offset[n] + offset[i] + j, to_ex, cost);
                }
                if to == n - 1 {
                    dijk.add_edge(inc * offset[n] + offset[i] + j,
                                  2 * offset[n], cost);
                }
            }
        }
    }
    for i in 0..n {
        if g[i].is_empty() { continue; }
        for j in 0..g[i].len() - 1 {
            dijk.add_edge(offset[n] + offset[i] + j,
                          offset[n] + offset[i] + j + 1, 0);
            dijk.add_edge(offset[i] + j + 1,
                          offset[i] + j, 0);
        }
    }
    let mut init = vec![];
    for i in 0..g[0].len() {
        init.push(i);
        init.push(offset[n] + i);
    }
    const INF: i64 = 1 << 50;
    let sol = dijk.solve(&init, INF);
    let mi = sol[2 * offset[n]];
    puts!("{}\n", if mi >= INF { -1 } else { mi });
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
