// f(x) := x == 0 ? 1 : (x から始めたときの期待回数) とし、f_n(x) = f(n + x) (0 < x <= 1) とする。
// f_n'(x) = f_n(x) - f_{n-1}(x) であるため、
// これを利用してある有理数列 a に対して
