fn main() {
    let n: usize = get();
    let (_, invfac) = fact_init(n + 1);
    println!("0");
    for i in (0..n).rev() {
        println!("{}", MInt::new((n - i) as i64).pow(i as i64) * invfac[i] * if i % 2 == 0 {
            1
        } else {
            MOD - 1
        });
    }
}
