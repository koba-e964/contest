// \pm 1, ..., \pm 2^n
fn calc(del: i64, n: i64, memo: &mut HashMap<(i64, i64), i64>) -> i64 {
    if n == 0 {
        return del.abs();
    }
    if let Some(&val) = memo.get(&(del, n)) {
        return val;
    }
    if del % 2 == 0 {
        let val = calc(del / 2, n - 1, memo);
        memo.insert((del, n), val);
        return val;
    }
    let mut val = del.abs();
    val.chmin(calc((del - 1) / 2, n - 1, memo) + 1);
    val.chmin(calc((del + 1) / 2, n - 1, memo) + 1);
    memo.insert((del, n), val);
    val
}

// Tags: limited-state-space
fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    input! {
        x: i64, y: i64,
    }
    let mut mi = (x - y).abs();
    let mut nx = x;
    let mut memo = HashMap::new();
    for i in 0..61 {
        let del = y - nx;
        mi.chmin(i + calc(del, i, &mut memo));
        if nx >= y {
            break;
        }
        nx *= 2;
    }
    puts!("{}\n", mi);
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
