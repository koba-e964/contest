/**
 * Union-Find tree.
 * Verified by https://atcoder.jp/contests/pakencamp-2019-day3/submissions/9253305
 */
struct UnionFind { disj: Vec<usize>, rank: Vec<usize> }

impl UnionFind {
    fn new(n: usize) -> Self {
        let disj = (0..n).collect();
        UnionFind { disj: disj, rank: vec![1; n] }
    }
    fn root(&mut self, x: usize) -> usize {
        if x != self.disj[x] {
            let par = self.disj[x];
            let r = self.root(par);
            self.disj[x] = r;
        }
        self.disj[x]
    }
    fn unite(&mut self, x: usize, y: usize) {
        let mut x = self.root(x);
        let mut y = self.root(y);
        if x == y { return }
        if self.rank[x] > self.rank[y] {
            std::mem::swap(&mut x, &mut y);
        }
        self.disj[x] = y;
        self.rank[y] += self.rank[x];
    }
    #[allow(unused)]
    fn is_same_set(&mut self, x: usize, y: usize) -> bool {
        self.root(x) == self.root(y)
    }
    #[allow(unused)]
    fn size(&mut self, x: usize) -> usize {
        let x = self.root(x);
        self.rank[x]
    }
}

fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    input! {
        n: usize, m: usize,
        p: [(f64, f64); n],
        c: [(f64, f64, f64); m],
    }
    let mut mi = 1.0e18;
    for bits in 0..1 << m {
        let mut e = vec![];
        let mut cu = vec![];
        for i in 0..m {
            if (bits & 1 << i) != 0 {
                cu.push(c[i]);
            }
        }
        let m = cu.len();
        for i in 0..n {
            let (x, y) = p[i];
            for j in 0..i {
                let (z, w) = p[j];
                let dist = (x - z) * (x - z) + (y - w) * (y - w);
                e.push((dist.sqrt(), j, i));
            }
        }
        for i in 0..n {
            let (x, y) = p[i];
            for j in 0..m {
                let (z, w, r) = cu[j];
                let dist = (x - z) * (x - z) + (y - w) * (y - w);
                e.push(((dist.sqrt() - r).abs(), i, n + j));
            }
        }
        for i in 0..m {
            let (x, y, r1) = cu[i];
            for j in 0..i {
                let (z, w, r2) = cu[j];
                let dist = (x - z) * (x - z) + (y - w) * (y - w);
                let dist = dist.sqrt();
                let dist = if dist >= r1 + r2 {
                    dist - r1 - r2
                } else if dist < (r1 - r2).abs() {
                    (r1 - r2).abs() - dist
                } else {
                    0.0
                };
                e.push((dist, n + j, n + i));
            }
        }
        e.sort_by(|&x, &y| x.0.partial_cmp(&y.0).unwrap());
        let mut uf = UnionFind::new(n + m);
        let mut tot = 0.0;
        for (d, v, w) in e {
            if !uf.is_same_set(v, w) {
                tot += d;
                uf.unite(v, w);
            }
        }
        mi.chmin(tot);
    }
    puts!("{}\n", mi);
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
