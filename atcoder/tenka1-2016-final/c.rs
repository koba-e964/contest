fn main() {
    input! {
        s: chars,
        m: usize,
        p: [chars; m],
        w: [i64; m],
    }
    let mut hm = HashMap::new();
    let mut bases = [MInt::new(0); 2];
    let mut rng = Rng::new();
    for i in 0..2 {
        bases[i] = MInt::new((rng.next() >> 32) as i64);
    }
    for i in 0..m {
        let mut hash = [MInt::new(0); 2];
        for &c in &p[i] {
            for j in 0..2 {
                hash[j] = hash[j] * bases[j] + c as u8 as i64;
            }
        }
        hm.entry(hash).or_insert(0).chmax(w[i]);
    }
    let n = s.len();
    let mut dp = vec![0; n + 1];
    for i in 1..n + 1 {
        dp[i] = dp[i - 1];
        let mut hash = [MInt::new(0); 2];
        let mut cur = [MInt::new(1); 2];
        for l in 1..=min(i, 200) {
            for k in 0..2 {
                hash[k] += cur[k] * s[i - l] as u8 as i64;
                cur[k] *= bases[k];
            }
            if let Some(&w) = hm.get(&hash) {
                dp[i] = max(dp[i], dp[i - l] + w);
            }
        }
    }
    println!("{}", dp[n]);
}
