/**
 * Union-Find tree.
 * Verified by https://atcoder.jp/contests/pakencamp-2019-day3/submissions/9253305
 */
struct UnionFind { disj: Vec<usize>, rank: Vec<usize> }

impl UnionFind {
    fn new(n: usize) -> Self {
        let disj = (0..n).collect();
        UnionFind { disj: disj, rank: vec![1; n] }
    }
    fn root(&mut self, x: usize) -> usize {
        if x != self.disj[x] {
            let par = self.disj[x];
            let r = self.root(par);
            self.disj[x] = r;
        }
        self.disj[x]
    }
    fn unite(&mut self, x: usize, y: usize) {
        let mut x = self.root(x);
        let mut y = self.root(y);
        if x == y { return }
        if self.rank[x] > self.rank[y] {
            std::mem::swap(&mut x, &mut y);
        }
        self.disj[x] = y;
        self.rank[y] += self.rank[x];
    }
    #[allow(unused)]
    fn is_same_set(&mut self, x: usize, y: usize) -> bool {
        self.root(x) == self.root(y)
    }
    #[allow(unused)]
    fn size(&mut self, x: usize) -> usize {
        let x = self.root(x);
        self.rank[x]
    }
}

// Tags: least-common-ancestors, hl-decomposition
fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    input! {
        n: usize, m: usize,
        abc: [(usize1, usize1, i64); m],
    }
    let mut e: Vec<_> = abc.iter().map(|&(a, b, c)| (c, a, b)).collect();
    e.sort();
    let mut uf = UnionFind::new(n);
    let mut tot = 0;
    let mut used = vec![];
    for &(c, a, b) in &e {
        if !uf.is_same_set(a, b) {
            uf.unite(a, b);
            tot += c;
            used.push((c, a, b));
        }
    }
    let mut pass = vec![0; m];
    let mut fail = vec![1i64 << 32; m];
    for _ in 0..32 {
        let mut qs = vec![];
        for i in 0..m {
            qs.push(((pass[i] + fail[i]) / 2, i));
        }
        for i in 0..used.len() {
            qs.push((used[i].0, m + i));
        }
        qs.sort();
        let mut uf = UnionFind::new(n);
        for &(w, idx) in &qs {
            if idx < m {
                let (a, b, _) = abc[idx];
                if uf.is_same_set(a, b) {
                    fail[idx] = w;
                } else {
                    pass[idx] = w;
                }
            } else {
                let idx = idx - m;
                let (_, a, b) = used[idx];
                uf.unite(a, b);
            }
        }
    }
    for i in 0..m {
        puts!("{}\n", tot - pass[i] + abc[i].2);
    }
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
