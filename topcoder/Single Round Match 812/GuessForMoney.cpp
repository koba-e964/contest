using namespace std;
typedef long long ll;

class GuessForMoney {
public:
  ll rec(ll n, map<ll, ll> &memo) {
    if (n <= 1) {
      return 0;
    }
    if (memo.count(n)) return memo[n];
    ll x = (n - 1) / 2;
    ll y = n - 1 - x;
    return memo[n] = rec(x, memo) + rec(y, memo) + n - 1;
  }
  double balance(long long n) {
    map<ll, ll> memo;
    return (double) rec(n, memo) / (double) n + 1;
  }
};

