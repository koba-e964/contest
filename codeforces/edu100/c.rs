trait Bisect<T> {
    fn lower_bound(&self, val: &T) -> usize;
    fn upper_bound(&self, val: &T) -> usize;
}

impl<T: Ord> Bisect<T> for [T] {
    fn lower_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] >= val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
    fn upper_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] > val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
}

const INF: i64 = 1 << 32;

fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    input! {
        tx: [[(i64, i64)]],
    }
    for tx in tx {
        let n = tx.len();
        let mut cv = vec![(0, 0)];
        let mut cur = 0;
        let mut rem = 0;
        let mut now = 0;
        for i in 0..n {
            let (t, x) = tx[i];
            // move until time = t
            if rem < 0 {
                let mi = min(-rem, t - now);
                rem += mi;
                cur -= mi;
                now += mi;
            } else {
                let mi = min(rem, t - now);
                rem -= mi;
                cur += mi;
                now += mi;
            }
            cv.push((now, cur));
            // command
            now = t;
            cv.push((now, cur));
            if rem == 0 {
                rem = x - cur;
            }
        }
        // move until time = inf
        if rem < 0 {
            let mi = min(-rem, INF - now);
            cur -= mi;
            now += mi;
        } else {
            let mi = min(rem, INF - now);
            cur += mi;
            now += mi;
        }
        cv.push((now, cur));
        // eprintln!("cv = {:?}", cv);
        let mut suc = 0;
        for i in 0..n {
            let (t, x) = tx[i];
            let tnxt = if i + 1 < n { tx[i + 1].0 } else { INF };
            let idx0 = cv.lower_bound(&(t, -INF));
            let idx1 = cv.upper_bound(&(tnxt, INF));
            let mut ok = false;
            for j in idx0..idx1 - 1 {
                if (cv[j].1 - x) * (x - cv[j + 1].1) >= 0 {
                    ok = true;
                }
            }
            if ok {
                suc += 1;
            }
        }
        puts!("{}\n", suc);
    }
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
