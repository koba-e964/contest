// Tags: interval cycle
fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    input! {
        n: usize,
        lr: [(usize1, usize1); n],
    }
    let mut occ = vec![vec![]; n];
    for i in 0..n {
        let (l, r) = lr[i];
        occ[l].push((r, i));
    }
    let mut que = BinaryHeap::new();
    let mut ans = vec![0; n];
    for i in 0..n {
        for &(r, idx) in &occ[i] {
            que.push(Reverse((r, i, idx)));
        }
        let Reverse((r, _l, idx)) = que.pop().unwrap();
        assert!(r >= i);
        ans[idx] = i;
    }
    let mut st = SegTree::new(n, min, (n + 1, 0usize));
    for i in 0..n {
        st.update(ans[i], (lr[i].0, i));
    }
    let mut found = None;
    for i in 0..n {
        // Check if there exists j s.t. ans[i] < ans[j] <= r[i] && l[j] <= ans[i]
        let (mi, idx) = st.query(ans[i] + 1, lr[i].1 + 1);
        if ans[i] >= mi {
            found = Some((i, idx));
            break;
        }
    }
    if let Some((i, j)) = found {
        puts!("No\n");
        for i in 0..n {
            puts!("{}{}", ans[i] + 1, if i + 1 == n { "\n" } else { " " });
        }
        ans.swap(i, j);
        for i in 0..n {
            puts!("{}{}", ans[i] + 1, if i + 1 == n { "\n" } else { " " });
        }
        
    } else {
        puts!("Yes\n");
        for i in 0..n {
            puts!("{}{}", ans[i] + 1, if i + 1 == n { "\n" } else { " " });
        }
    }
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
