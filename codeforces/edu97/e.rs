trait Bisect<T> {
    fn lower_bound(&self, val: &T) -> usize;
    fn upper_bound(&self, val: &T) -> usize;
}

impl<T: Ord> Bisect<T> for [T] {
    fn lower_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] >= val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
    fn upper_bound(&self, val: &T) -> usize {
        let mut pass = self.len() + 1;
        let mut fail = 0;
        while pass - fail > 1 {
            let mid = (pass + fail) / 2;
            if &self[mid - 1] > val {
                pass = mid;
            } else {
                fail = mid;
            }
        }
        pass - 1
    }
}

const INF: i64 = 1 << 30;

fn calc(a: &[i64], lo: i64, hi: i64) -> usize {
    let mut b = vec![];
    for &a in a {
        if lo <= a && a <= hi {
            b.push(a);
        }
    }
    // Find LIS
    let mut dp = vec![-INF];
    let mut maxlen = 0;
    for i in 0..b.len() {
        let idx = dp.upper_bound(&b[i]) - 1;
        if idx == maxlen {
            dp.push(INF);
            maxlen += 1;
        }
        dp[idx + 1] = min(dp[idx + 1], b[i]);
    }
    a.len() - maxlen
}

// Tags: longest-increasing-sequence
fn solve() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {
        ($($format:tt)*) => (let _ = write!(out,$($format)*););
    }
    #[allow(unused)]
    macro_rules! putvec {
        ($v:expr) => {
            for i in 0..$v.len() {
                puts!("{}{}", $v[i], if i + 1 == $v.len() {"\n"} else {" "});
            }
        }
    }
    macro_rules! fail {
        () => {
            puts!("-1\n");
            return;
        }
    }
    input! {
        n: usize, k: usize,
        a: [i64; n],
        b: [usize1; k],
    }
    let mut a = a;
    for i in 0..n {
        a[i] -= i as i64;
    }
    if k > 0 {
        for i in 0..k - 1 {
            if a[b[i]] > a[b[i + 1]] {
                fail!();
            }
        }
    }
    let mut tot = 0;
    if k == 0 {
        tot += calc(&a, -INF, INF);
    } else {
        tot += calc(&a[..b[0]], -INF, a[b[0]]);
        for i in 0..k - 1 {
            tot += calc(&a[b[i] + 1..b[i + 1]], a[b[i]], a[b[i + 1]]);
        }
        tot += calc(&a[b[k - 1] + 1..], a[b[k - 1]], INF);
    }
    puts!("{}\n", tot);
}

fn main() {
    // In order to avoid potential stack overflow, spawn a new thread.
    let stack_size = 104_857_600; // 100 MB
    let thd = std::thread::Builder::new().stack_size(stack_size);
    thd.spawn(|| solve()).unwrap().join().unwrap();
}
